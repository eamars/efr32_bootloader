from threading import Lock

class ThreadSafeDict(dict) :
    '''Reference: http://stackoverflow.com/a/29532297'''
    def __init__(self, * p_arg, ** n_arg) :
        dict.__init__(self, * p_arg, ** n_arg)
        self._lock = Lock()

    def __enter__(self) :
        self._lock.acquire()
        return self

    def __exit__(self, type, value, traceback) :
        self._lock.release()
